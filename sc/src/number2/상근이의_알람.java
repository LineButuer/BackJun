package number2;

import java.util.Scanner;

public class 상근이의_알람 {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        int a = scanner.nextInt(), b = scanner.nextInt();
        if(a!=0&&b==b){
            int c = a-1;
            int d = b+15;
            System.out.println(c+" "+d);
        } else if (a==0&&b==b) {
            a=23;
            int d = b+15;
            System.out.println(a +" "+d);
        }
    }
}
